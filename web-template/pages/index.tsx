import Head from "next/head";

import stockone from "../assets/stock_1.jpg";
import stockthree from "../assets/stock_3.jpg";
import stocktwo from "../assets/stock_2.jpg";
import Image from "next/image";
import ResuableModal from "../shared/components/modal";

import Link from "next/link";
import { useCountdown } from "../useCountdown";
import { useState } from "react";

export const pages: any[] = [
  {
    name: "services",
    photo: stockone,
    snippet: "We offer a variety of services.",
  },
  {
    name: "gallery",
    photo: stockthree,
    snippet: "Got ideas? Check out our previous projects",
  },
  {
    name: "about us",
    photo: stocktwo,
    snippet: "Meet the team",
  },
];
export default function Home() {
  const [openModal, setOpenModal] = useState(false);
  const [days, hours, minutes, seconds] = useCountdown();

  const DateTimeDisplay = ({ value, type }) => {
    return (
      <div>
        {type}
        <br />
        {value}
      </div>
    );
  };
  const ShowCounter = ({ days, hours, minutes, seconds }) => {
    return (
      <div className="countDown">
        <DateTimeDisplay type={"Days"} value={days} />

        <DateTimeDisplay type={"Hours"} value={hours} />

        <DateTimeDisplay type={"Minutes"} value={minutes} />

        <DateTimeDisplay type={"Seconds"} value={seconds} />
      </div>
    );
  };
  const CountdownTimer = ({ targetDate }) => {
    console.log(targetDate);
    const [days, hours, minutes, seconds] = useCountdown(targetDate);
    return (
      <ShowCounter
        days={days}
        hours={hours}
        minutes={minutes}
        seconds={seconds}
      />
    );
  };
  return (
    <>
      <Head>
        <title>Web-Template | Home</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <section className="content">
        {" "}
        <CountdownTimer targetDate={20230625} />
        {pages.map((page) => (
          <>
            <div className="inner-content">
              {" "}
              <div className="column">
                <Link href={`/${page.name}`}>
                  <Image
                    alt="stock one"
                    src={page.photo}
                    width={300}
                    height={250}
                  />{" "}
                </Link>
              </div>
              <div className="column">
                <Link href={`/${page.name}`}>
                  <h1>{page.name}</h1>
                  <p>{page.snippet}</p>
                </Link>
              </div>
            </div>
          </>
        ))}
      </section>
      <section className="quote-content">
        <div className="inner-quote-content">
          <h1>Need a Quote? </h1>
          <p>
            Send us an message and we can schedule a time to get you a free
            estimate!
          </p>
          <button onClick={() => setOpenModal(true)}>contact us</button>
          {openModal && (
            <ResuableModal setModalOpen={setOpenModal} openModal={openModal} />
          )}
        </div>
      </section>
    </>
  );
}
